<script>
    $(document).on('click', '.tab-link', function() {
        let tabId = $(this).data('tab');
        // Remove 'active' class from all tabs and tab contents
        $('.tab-link').removeClass('active');
        $('.tab-content').removeClass('active');

        // Add 'active' class to the clicked tab and its corresponding content
        $(this).addClass('active');
        $(`#tab-${tabId}`).addClass('active');
        get_resource(tabId);
    });

    function get_resource(tabId) {
        var v_token = "{{ csrf_token() }}"; // Token for CSRF protection

        // Fetch resource details based on the clicked tab
        $.ajax({
            url: '{{ route("get-resource-by-category") }}',
            method: 'POST',
            data: {
                category_id: tabId,
                _token: v_token
            },
            success: function(response) {
                if (response.status) {
                    let resources = response.data;
                    let resourcesHtml = '';
                    let headr = `<div id="tab-${tabId}" class="tab-content  active mr_top_30"><div class="row">`;
                    let footr = `</div></div>`;
                    // Generate HTML for the resources
                    resources.forEach(function(resource) {
                        resourcesHtml += `
                        <div class="col-lg-6">
                            <div class="tllss_box">
                                <h4>${resource.title}
                                    <div class="ribbon-wrap">
                                        <div class="ribbon">${resource.ribbon}</div>
                                    </div>
                                </h4>
                                <p>${resource.description}
                                    <a href="javascript:void(0);" class="text-right" onclick="readMore(${resource.id})">.... Explor More</a>
                                </p>
                            </div>
                        </div>
                    `;
                    });
                    let contentHtml = headr + resourcesHtml + footr;
                    // Inject the generated HTML into the DOM
                    $('#resource-content').html(contentHtml);
                } else {
                    console.log('No resources found.');
                }
            },
            error: function(xhr, status, error) {
                console.log('Error fetching resources:', error);
            }
        });
    }

    function readMore(resource_id) {
        var loc = '{{ route('check-authorization', ['id' => ':id']) }}';
        loc = loc.replace(':id', resource_id);
        window.location.href = loc;
    }
</script>